<h2 style="font-size: 18px">The EUPL &quot;Downstream&quot; compatibility list</h2>
<p><font size="2">The EUPL v1.1 downstream compatibility list was established for the EUPL v1.0 (January 2007). It was drafted based on a September 2006 study of the CRID (Research Centre in Computers and Law &ndash; FUNDP Namur)</font><a href="#notes"><font size="2"><span><span style="color: #17365d">[6]</span></span></font></a></p>
<div style="margin: 0cm 0cm 12pt"><span><font size="2">The purpose of the list is to allow mergers between EUPLed code and other copylefted code (when license terms impose to redistribute any larger, merged work under the same &ldquo;inherited&rdquo; license, distribution becomes legally impossible in case of copyleft conflicts). Therefore the chosen licenses had to be in any case copyleft, and &ldquo;the copyleft effect of the elected licenses should be similar to the EUPL&rsquo;s copyleft and should fulfill the same functions&rdquo;, the authors report&nbsp;</font><a href="#notes"><font color="#0000ff"><font size="2"><span>[7]</span></font></font></a></span></div>
<div style="margin: 0cm 0cm 12pt"><font size="2">The study proposed the following list:</font></div>
<ul>
    <li><font size="2">General Public License (GPL) v. 2</font></li>
    <li><font size="2">Open Software License (OSL) v. 2.1, v. 3.0</font></li>
    <li><font size="2">Common Public License (CPL) v. 1.0</font></li>
    <li><font size="2">Eclipse Public License (EPL) v. 1.0</font></li>
    <li><span><span style="font: 7pt \\\'Times New Roman\\\'">&nbsp;</span></span><font size="2">Cecill v. 2.0</font></li>
</ul>
<div><span><font size="2">The presence of the strong copyleft (OSI-approved) GPL and OSL makes no discussion. The presence of non-OSI-approved CeCILL was interesting because supported by three French public research centres, strong copyleft and &ndash; by the way &ndash; permitting re-licensing under all GPL versions (including the GPLv3 that was published in June 2007) This makes the EUPL compatible, directly or indirectly, with all GPL versions.&nbsp;What looks more questionnable is the presence of the Eclipse (and of the CPL, but we may forget this one that has now been superseded by the EPL), because according to the Free Software Foundation, the EPL only provides a &ldquo;weaker copyleft&rdquo;.<span style="color: #17365d"> </span></font></span></div>
<div>&nbsp;</div>
<div style="margin: 0cm 0cm 12pt"><font size="2">When analyzing copyleft, the study made three categories</font><a href="file:///C:/Documents%20and%20Settings/schmitzp/My%20Documents/EUPL%20&amp;amp;%20FOSS/New%20OSOR%20legal%20section/EUPL%20compatibility%20matrix/General%20EUPL%20compatibility%20Matrix%20v043.docx"><font size="2"><span><span style="color: #17365d">[8]</span></span></font></a></div>
<ul>
    <li><font size="2"><font size="2"><strong><span style="color: #17365d">Weak copyleft </span></strong>when&nbsp;the&nbsp;licence restricts the effect on a file basis (this is the case of the Mozilla Public License and the Common Development and Distribution License).</font></font><font size="2">:</font></li>
    <li><font size="2"><strong><span style="color: #17365d">Source only copyleft</span></strong>&nbsp;when&nbsp;the&nbsp;licence imposes the redistribution of the source code under the same licence, while the executable code may be governed by another licence (as long as the source code is available and remains under the original licence): the executable version of the derivative work can then be proprietary (this is the case of the EPL)</font></li>
    <li><font size="2"><strong><span style="color: #17365d">Strong copyleft</span></strong> when the licence does not restrict the copyleft effect to modifications on a file basis, and the executable is to be considered as a derivative work.</font></li>
</ul>
