  $query = "INSERT INTO term_data (vid, name, weight) VALUES (%d, '%s', %d)";
  db_query($query, $vocabulary, $name, $value[2]);
  $tid = db_last_insert_id('term_data', 'tid');

  //Retrieve the hierarchy (depends on numbers in name)
  if (is_numeric(substr($name, 0, 4))) {
    $second_parent = $tid;
    $parent = $first_parent;
  }
  elseif (is_numeric(substr($name, 0, 2))) {
    $first_parent = $tid;
    $parent = 0;
  }
  else {
    $parent = $second_parent;
  }

  //Insert the tid in term_hierarchy
  $query = "INSERT INTO term_hierarchy (tid, parent) VALUES (%d, %d)";
  db_query($query, $tid, $parent);

  //Insert the tid + uri in term_fields_term
  if (!empty($term_uri)) {
    $query = "INSERT INTO term_fields_term (tid, vid, taxonomy_uri_value) VALUES (%d, %d, '%s')";
    db_query($query, $tid, $vocabulary, $term_uri);
  }

  //Mapping old term nodes
  if (isset($value[3])) {
    $old_terms = array_merge($old_terms, $value[3]);
    $values = implode(',', $value[3]);
    $query = "SELECT * FROM term_node WHERE tid IN (%s)";
    $results = db_query($query, $values);
    while ($result = db_fetch_array($results)) {
      $insert = "INSERT INTO term_node (nid, vid, tid) VALUES (%d,%d,%d)";
      db_query($insert, intval($result['nid']), intval($result['vid']), intval($tid));
    }
  }
}
